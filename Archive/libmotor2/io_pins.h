/*
  Propeller I/O pin definitions

*/


#define VR_LED    1                 // Green EasyVR Sampling LED (Blink with ERR_LED for Trigger)
#define ERR_LED   2                 // Red EasyVR Error LED (Blink with VR_LED for Trigger)
#define LCD_PIN   3                 // LCD display data pin
#define LENC_PIN  4                 // Left wheel encoder data pin
#define RENC_PIN  5                 // Right wheel encoder data pin

#define VR_RX_PIN 7                 // EasyVR Recv pin
#define VR_TX_PIN 8                 // EasyVR Xmit pin
#define CSCL_PIN  9                 // Compass SCL pin
#define CSDA_PIN  10                // Compass SDA pin
#define PING_PIN  11                // Ultrasonic Ping))) sensor
#define PIR_PIN   12                // Passive IR (PIR) sensor
#define ACCY_PIN  13                // Memsic 2125 Dual-axis Accelerometer Y-Axis
#define ACCX_PIN  14                // Memsic 2125 Dual-axis Accelerometer X-Axis
#define HEAD_PIN  15                // Head servo pin
#define LT_WHEEL  16                // Left wheel continuos rotation servo
#define RT_WHEEL  17                // Right wheel continuos rotation servo

